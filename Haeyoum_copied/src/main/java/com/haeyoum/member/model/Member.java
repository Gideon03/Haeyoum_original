package com.haeyoum.member.model;				
				
import java.util.Date;				
				
public class Member {				
				
	private String member_id;			
	private String member_password;			
	private String member_image;			
	private String member_name;			
	private String member_nickname;			
	private String member_email;			
	private Date member_birthday;			
	private Date member_regdate;			
	private Date member_moddate;			
				
	public Member() {			
	}			
				
	public String getMember_id() {			
		return member_id;		
	}			
				
	public void setMember_id(String member_id) {			
		this.member_id = member_id;		
	}			
				
	public String getMember_password() {			
		return member_password;		
	}			
				
	public void setMember_password(String member_password) {			
		this.member_password = member_password;		
	}			
				
	public String getMember_image() {			
		return member_image;		
	}			
				
	public void setMember_image(String member_image) {			
		this.member_image = member_image;		
	}			
				
	public String getMember_name() {			
		return member_name;		
	}			
				
	public void setMember_name(String member_name) {			
		this.member_name = member_name;		
	}			
				
	public String getMember_nickname() {			
		return member_nickname;		
	}			
				
	public void setMember_nickname(String member_nickname) {			
		this.member_nickname = member_nickname;		
	}			
				
	public String getMember_email() {			
		return member_email;		
	}			
				
	public void setMember_email(String member_email) {			
		this.member_email = member_email;		
	}			
				
	public Date getMember_birthday() {			
		return member_birthday;		
	}			
				
	public void setMember_birthday(Date member_birthday) {			
		this.member_birthday = member_birthday;		
	}			
				
	public Date getMember_regdate() {			
		return member_regdate;		
	}			
				
	public void setMember_regdate(Date member_regdate) {			
		this.member_regdate = member_regdate;		
	}			
				
	public Date getMember_moddate() {			
		return member_moddate;		
	}			
				
	public void setMember_moddate(Date member_moddate) {			
		this.member_moddate = member_moddate;		
	}
	
	
				
}				